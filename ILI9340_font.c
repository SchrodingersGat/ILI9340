unsigned char char_offset(char c)
{
    if (c > 126 || c < 33) c = 33;

    return (unsigned char) c - 33;
}

unsigned char char_width(char c)
{
    return (unsigned char) CHAR_LOOKUP[char_offset(c)].charWidth;
}


unsigned short char_address(char c)
{
    return CHAR_LOOKUP[char_offset(c)].charOffset;
}

unsigned short line_width(char *c)
{
    unsigned short w = 0;

    unsigned short i=0;

    while (c[i] != 0)
    {
    	if (c[i] == '\n')
    	{
    		break; //end of line
    	}
    	else if (c[i] == ' ') //space
    	{
    		w += FONT_SPACE_SIZE;
    	}
    	else if (c[i] > 32 && c[i] < 127) // valid range
    	{
    		w += char_width(c[i]);

    		w += FONT_CHAR_GAP;
    	}

    	i++; //increment character
    }

    return w;
}


